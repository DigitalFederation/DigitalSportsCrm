    public function setUp(): array
    {
        // $this->showCheckBox();

        return [
            Header::make()
                ->showSearchInput(),
            Footer::make()
                ->showPerPage()
                ->showRecordCount(),
        ];
    }

    /*
    |--------------------------------------------------------------------------
    |  Datasource
    |--------------------------------------------------------------------------
    | Provides data to your Table using a Model or Collection
    |
    */

    /**
     * PowerGrid datasource.
     *
     * @return Builder<\Domain\Individuals\Models\Individual>
     */
    public function datasource(): Builder
    {
        $federation = auth()->user()->federations()->first();

        return Individual::query()->whereHas('individualFederations', function ($query) use ($federation) {
            return $query->where('federation_id', $federation->id)
                ->where('active', true);
        });
    }

